// Да се напише програма, която изписва в конзолата сумата на четните числа, които се делят на 3, в интервала [1 - 20] включително.


// очакван отговор: sum = 36


let evenNumberSum = 0;

for (let i = 1; i <= 20; i++) {
    if (i%2===0 && i%3===0) {
        evenNumberSum+=i;
    }
}

console.log(`Сумата на четните числа, които се делят на 3 в интервала [1 - 20] е: ${evenNumberSum}`);
